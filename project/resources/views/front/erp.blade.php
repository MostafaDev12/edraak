    <div class="erp-modules" id="erp-modules">
        <div class="container">
            <h2>ERP Modules</h2>
            <div class="row">
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Finance</h4>
                        <p>A finance module, the foundation of just about every ERP system, manages the general ledger and all financial data. It tracks every transaction, including accounts payable (AP) and accounts receivable (AR), and handles reconciliations and financial reporting.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Procurement</h4>
                        <p>The procurement module manages purchasing, whether raw materials or finished goods. It can automate requests for quotes and purchase orders and, when linked to demand planning, minimize overbuying and underbuying.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Manufacturing</h4>
                        <p>Manufacturing can be complicated, and this module helps companies coordinate all the steps that go into making products. The module can ensure production is in line with demand and monitor the number of in-progress and finished items.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Inventory management</h4>
                        <p>An inventory management module shows current inventory levels down to the SKU level and updates those numbers in real time. It also measures key inventory-related metrics. Any products-based company needs this module to optimize stock on-hand based on current and forecasted demand.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Order management</h4>
                        <p>This application monitors and prioritizes customer orders from all channels as they come in and tracks their progress through delivery. An order management module can speed fulfillment and delivery times and improve the customer experience.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Warehouse management</h4>
                        <p>A warehouse management module directs warehouse activities like receiving, picking, packing and shipping. It can generate time and cost savings in the warehouse by identifying more efficient ways to execute these tasks.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Customer relationship management (CRM)</h4>
                        <p>CRM is a popular module for businesses in a wide range of industries. It tracks all communications with clients, assists with lead management and can enhance customer service and boost sales.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Professional services automation (PSA)</h4>
                        <p>Services businesses often utilize a professional services automation (PSA) module to plan and track projects, including the time and resources spent on them. It can simplify client billing and encourage collaboration among staff members working on a project.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Workforce management (WFM)</h4>
                        <p>A workforce management (WFM) module keeps track of attendance and hours worked, and some can also manage payroll. This tool can record absenteeism and productivity by department, team and individual employee.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Human resources management (HRM)</h4>
                        <p>A human resources management (HRM) or human capital management (HCM) module version of a WFM module. It keeps employee records with detailed information, like available PTO and performance reviews, and can tease out workforce trends in various departments or demographics.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Ecommerce</h4>
                        <p>An ecommerce module allows retailers and brands to manage the back- and front-ends of their online stores. They can change the site look and feel and add and update product pages with this application.</p>
                    </div>
                </div>
                <div class="col-lg-4 col-md-6 mb-4">
                    <div class="erp-module-box">
                        <h4>Marketing automation</h4>
                        <p>This module manages marketing efforts across all digital channels—email, web, social—and enables organizations to optimize and personalize their messaging. A marketing automation tool can boost leads, sales and customer loyalty.</p>
                    </div>
                </div>
            </div>
